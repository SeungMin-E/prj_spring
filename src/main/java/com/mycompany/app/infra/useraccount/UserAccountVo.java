package com.mycompany.app.infra.useraccount;

public class UserAccountVo {
	private String seq;
	private String userNameWord;
	private String userGenOption;
	private String userDelNy;
	private String userAgeOption;
	
//	get & set
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getUserNameWord() {
		return userNameWord;
	}
	public void setUserNameWord(String userNameWord) {
		this.userNameWord = userNameWord;
	}
	public String getUserGenOption() {
		return userGenOption;
	}
	public void setUserGenOption(String userGenOption) {
		this.userGenOption = userGenOption;
	}
	public String getUserDelNy() {
		return userDelNy;
	}
	public void setUserDelNy(String userDelNy) {
		this.userDelNy = userDelNy;
	}
	public String getUserAgeOption() {
		return userAgeOption;
	}
	public void setUserAgeOption(String userAgeOption) {
		this.userAgeOption = userAgeOption;
	}
	
	
}
